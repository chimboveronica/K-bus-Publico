                    . "text:'" . utf8_encode($myrow["ruta"]) . "',"
                    . "codRuta: '". $myrow["cod_ruta"]. "'},";
        }

        $objJson .="]}";
        echo $objJson;
    } else {
        echo "{success:false, msg: 'No hay datos que obtener'}";
    }
